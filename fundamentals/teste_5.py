print("Programa de Comparacao de Numeros");

n_1 = int(input("Digite o primeiro numero: "));
n_2 = int(input("Digite o segundo numero: "));

if n_1 > n_2:
    print("O primeiro numero  é maior que o segundo" );
elif n_1 == n_2:
    print("Os numeros digitados sao iguais");
else:
    print("O primeiro numero é menor que o segundo");
